package 二叉树的实现;
/**
 * 二叉树的实现方法
 * 链表的方法实现
 * @author mac1094
 *
 */
// 定义链表的声明类
public class TreeNode {
	// 树值
	int value;
	// 左子树
	TreeNode left_Node;
	// 右子树
    TreeNode right_Node;
    
    // 构造方法
	public TreeNode(int value) {
		super();
		this.value = value;
		this.left_Node = null;
		this.right_Node = null;
	}
    
    
    
}
